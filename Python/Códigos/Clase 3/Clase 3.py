#Estructuras de control bucles ( for , while )
# El bucle "while"
# while = mientras

for i in range(5): # 0 1 2 3 4
    print(i)

numero = 0
while ( numero < 5 ):
    print(numero)
    numero = numero + 1

print("Fin")
# imprimir 0    numero = 1
# imprimir 1    numero = 2 
# imprimir 2    numero = 3
# imprimir 3    numero = 4
# imprimir 4    numero = 5

